package bit701.day0830;

public class OperEx6 {

	public static void main(String[] args) {
		int a, b;
		a = 7; 
		b = 4;
		System.out.println(a + b);
		System.out.println(a - b);
		System.out.println(a * b);
		if (b != 0) {
			System.out.println((double)a / b);
			System.out.println(a % b);
		}
	}

}
